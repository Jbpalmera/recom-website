<script>
    // Back to top functionality
    document.addEventListener('DOMContentLoaded', function() {
        const backToTopButton = document.getElementById('backToTop');

        // Show/hide back to top button based on scroll position
        window.addEventListener('scroll', function() {
            if (window.pageYOffset > 300) {
                backToTopButton.classList.remove('translate-y-16', 'opacity-0');
                backToTopButton.classList.add('translate-y-0', 'opacity-100');
            } else {
                backToTopButton.classList.remove('translate-y-0', 'opacity-100');
                backToTopButton.classList.add('translate-y-16', 'opacity-0');
            }
        });

        // Scroll to top when button is clicked
        backToTopButton.addEventListener('click', function() {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });

        // Newsletter subscription form handling
        const newsletterForm = document.querySelector('.bg-gray-800\\/50 form');
        if (newsletterForm) {
            newsletterForm.addEventListener('submit', function(e) {
                e.preventDefault();
                const emailInput = this.querySelector('input[type="email"]');
                if (emailInput.value) {
                    // In a real application, you would send this to your backend
                    alert('Thank you for subscribing! You will receive updates at ' + emailInput.value);
                    emailInput.value = '';
                }
            });
        }
    });
</script>

